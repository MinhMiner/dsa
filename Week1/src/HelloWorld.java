public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello, World");
    }
}

/*
 * Nếu bỏ từ khóa 'public' của class HelloWorld: Chương trình vẫn có thể chạy
 * Nếu bỏ từ khóa 'public' hoặc 'static' của hàm main: Thông báo lỗi không thể tìm thấy hàm main
 * Nếu bỏ từ khóa 'void': Lỗi cú pháp
 * Nếu bỏ từ khóa 'args': Lỗi
 */